#pragma once

// Address map
#define SOCCTRL_BASE_ADDR     0x03000000
#define UART_BASE_ADDR        0x03002000
#define GPIO_BASE_ADDR        0x03005000
#define TIMER_BASE_ADDR       0x0300A000
#define USER_DOMAIN_BASE_ADDR 0x20000000

// SIMPLE_CNT
#define SIMPLE_CNT_START_ADDRESS USER_DOMAIN_BASE_ADDR + 0x00000000
#define SIMPLE_CNT_SIZE 0x00010000
#define SIMPLE_CNT_END_ADDRESS SIMPLE_CNT_START_ADDRESS + 0x00010000

// CONV1D_ACCEL
#define CONV1D_ACCEL_START_ADDRESS USER_DOMAIN_BASE_ADDR + 0x00010000
#define CONV1D_ACCEL_SIZE 0x00010000
#define CONV1D_ACCEL_END_ADDRESS CONV1D_ACCEL_START_ADDRESS + 0x00010000

// SIMPLE_CNT CSR
#define SIMPLE_CNT_PERIPH_START_ADDRESS CONV1D_ACCEL_END_ADDRESS + 0x0000000
#define SIMPLE_CNT_PERIPH_SIZE 0x0001000
#define SIMPLE_CNT_PERIPH_END_ADDRESS SIMPLE_CNT_PERIPH_START_ADDRESS + 0x0001000

// CONV1D_ACCEL CSR
#define CONV1D_ACCEL_PERIPH_START_ADDRESS CONV1D_ACCEL_END_ADDRESS + 0x00001000
#define CONV1D_ACCEL_PERIPH_SIZE 0x00001000
#define CONV1D_ACCEL_PERIPH_END_ADDRESS CONV1D_ACCEL_PERIPH_START_ADDRESS + 0x00001000

// Frequencies
#define TB_FREQUENCY 20000000
#define TB_BAUDRATE    115200

// Peripheral configs
// UART
#define UART_BYTE_ALIGN 4
#define UART_FREQ       TB_FREQUENCY
#define UART_BAUD       TB_BAUDRATE